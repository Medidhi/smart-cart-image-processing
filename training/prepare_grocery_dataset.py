#!/usr/bin/env python3
"""
Convert the GroceryStore dataset to YOLO detection format.

Source: https://github.com/marcusklasson/GroceryStoreDataset  (WACV 2019).
That dataset is a *classification* set (one label per image, no bounding boxes). Since
YOLO detection needs boxes, each image is given ONE full-frame box
(`<class> 0.5 0.5 1.0 1.0`) labeled with its COARSE class. The photos are mostly a
single centered item, so a full-frame box is a reasonable weak label for a demo/sample
detection dataset. For production accuracy, replace these with real boxes (e.g. relabel
in Roboflow).

The raw dataset is git-cloned from GitHub automatically.

Usage:
    python3 prepare_grocery_dataset.py                 # clone from GitHub, all splits
    python3 prepare_grocery_dataset.py --limit 40      # <=40 images/split (quick demo)
    python3 prepare_grocery_dataset.py --fine          # 81 fine classes instead of coarse
    python3 prepare_grocery_dataset.py --keep-source   # keep the cloned repo afterwards

Result under training/datasets/grocery/:
    images/{train,val,test}/*.jpg   labels/{train,val,test}/*.txt
and a regenerated data.yaml whose `names` match the classes actually written.
"""

import argparse
import csv
import shutil
import subprocess
from pathlib import Path

HERE = Path(__file__).resolve().parent
DATASETS = HERE / "datasets"
SRC = DATASETS / "_grocerystore_src"          # default cloned repo (gitignored)
OUT = DATASETS / "grocery"                      # YOLO-format output
DATA_YAML = HERE / "data.yaml"
REPO = "https://github.com/marcusklasson/GroceryStoreDataset.git"
SPLITS = {"train": "train.txt", "val": "val.txt", "test": "test.txt"}
IMG_EXTS = {".jpg", ".jpeg", ".png"}


def clone_source() -> Path:
    """Clone the dataset repo if needed; return its `dataset/` dir."""
    if not (SRC / "dataset" / "classes.csv").exists():
        SRC.parent.mkdir(parents=True, exist_ok=True)
        print(f"[data] cloning {REPO} (~250 MB) ...")
        subprocess.run(["git", "clone", "--depth", "1", REPO, str(SRC)], check=True)
    else:
        print(f"[data] using existing clone: {SRC}")
    return SRC / "dataset"


def load_classes(ds_dir: Path, fine: bool):
    """Return (label_col_index, names) where names maps class_id -> class name."""
    fine_names, coarse_names = {}, {}
    with open(ds_dir / "classes.csv", newline="") as f:
        for row in csv.DictReader(f):
            fine_names[int(row["Class ID (int)"])] = row["Class Name (str)"]
            coarse_names[int(row["Coarse Class ID (int)"])] = row["Coarse Class Name (str)"]
    if fine:
        return 1, {i: fine_names[i] for i in sorted(fine_names)}   # col 1 = fine id
    return 2, {i: coarse_names[i] for i in sorted(coarse_names)}    # col 2 = coarse id


def flatten_name(rel_path: str) -> str:
    """train/Fruit/Apple/Golden-Delicious/Golden-Delicious_001.jpg -> Fruit__Apple__...__001.jpg"""
    without_split = rel_path.split("/", 1)[1] if "/" in rel_path else rel_path
    return without_split.replace("/", "__")


def convert(ds_dir: Path, label_col: int, limit):
    if OUT.exists():
        shutil.rmtree(OUT)
    counts = {}
    for split, txt in SPLITS.items():
        list_file = ds_dir / txt
        if not list_file.exists():
            print(f"[data] skip {split}: {txt} not found")
            continue
        img_out = OUT / "images" / split
        lbl_out = OUT / "labels" / split
        img_out.mkdir(parents=True, exist_ok=True)
        lbl_out.mkdir(parents=True, exist_ok=True)

        n = 0
        for line in list_file.read_text().strip().splitlines():
            parts = [p.strip() for p in line.split(",")]
            rel, class_id = parts[0], parts[label_col]
            src_img = ds_dir / rel
            if not src_img.exists():
                continue
            stem = flatten_name(rel)
            shutil.copy2(src_img, img_out / stem)
            (lbl_out / (Path(stem).stem + ".txt")).write_text(
                f"{int(class_id)} 0.5 0.5 1.0 1.0\n")
            n += 1
            if limit and n >= limit:
                break
        counts[split] = n
        print(f"[data] {split}: {n} images -> {img_out}")
    return counts


def write_data_yaml(names: dict):
    lines = [
        "# Auto-generated by prepare_grocery_dataset.py from the GroceryStore dataset.",
        "# Full-frame boxes (weak labels) — replace with real boxes for production.",
        "# `path` is relative to the Ultralytics datasets_dir, which train.py/the notebook",
        "# set to the training/ directory (see configure_datasets_dir in train.py).",
        "",
        "path: datasets/grocery",
        "train: images/train",
        "val: images/val",
        "test: images/test",
        "",
        "names:",
    ]
    lines += [f"  {i}: {name}" for i, name in names.items()]
    DATA_YAML.write_text("\n".join(lines) + "\n")
    print(f"[data] wrote {DATA_YAML} with {len(names)} classes")


def main():
    ap = argparse.ArgumentParser(description=__doc__,
                                 formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("--limit", type=int, default=None,
                    help="max images per split (for a quick smaller sample)")
    ap.add_argument("--fine", action="store_true",
                    help="use the 81 fine-grained classes (default: ~43 coarse classes)")
    ap.add_argument("--keep-source", action="store_true",
                    help="keep the cloned repo under datasets/_grocerystore_src")
    args = ap.parse_args()

    ds_dir = clone_source()

    label_col, names = load_classes(ds_dir, fine=args.fine)
    counts = convert(ds_dir, label_col, args.limit)
    write_data_yaml(names)

    if not args.keep_source:
        shutil.rmtree(SRC, ignore_errors=True)
        print(f"[data] removed source clone ({SRC.name}); pass --keep-source to retain it")

    total = sum(counts.values())
    print(f"\n[data] done: {total} images across {len(counts)} splits, "
          f"{len(names)} {'fine' if args.fine else 'coarse'} classes.")
    print("[data] next: open train_grocery.ipynb or run  python3 train.py")


if __name__ == "__main__":
    main()
